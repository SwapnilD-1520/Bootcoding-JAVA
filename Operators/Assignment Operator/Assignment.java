//Write a Java program to demonstrate the use of various assignment operators (+=, -=, *=, /=).

class Assignment
{		
	public static void main(String args[])
	
	{	
		int y = 50;
		y += 20;	

		System.out.println("Add & assign =" + y);
		y -= 20;	

		System.out.println("Subtract & assign =" + y);
		y *= 20;	

		System.out.println("multiply & assign =" + y);
		y /= 20;	

		System.out.println("Divide & assign =" + y);
	}	
	
}
